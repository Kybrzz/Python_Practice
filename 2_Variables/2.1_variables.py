# if u want to specify the data type of a var, use 'casting'

x = str(3)  # x is a str w/ a value of 3
y = int(5)  # y is a int w/ a value of 5
z = float(4)    # z is a float w/ a value of 4 

# obtain the data type of a var /w the 'type' function 
print(type(x))
print(type(y))
print(type(z)) 

